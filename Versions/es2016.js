//Exponentiation Operator -> The exponentiation operator (**) raises the first operand to the power of the second operand.
let x = 5;
let z = x ** 2;
console.log(z)

//Exponentiation Assignment -> The exponentiation assignment operator (**=) raises the value of a variable to the power of the right operand.
let y=5;
y **= 3
console.log(y)

//JavaScript Array includes() -> This allows us to check if an element is present in an array:
const fruits = ["Banana", "Orange", "Apple", "Mango"];

const check = fruits.includes("Mango");

console.log(check)
